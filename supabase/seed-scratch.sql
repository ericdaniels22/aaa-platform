    test_contact_id, aaa_org_id,
    'Scratch', 'Tester', '555-0100', 'scratch.tester@example.com', 'homeowner'
  )
  ON CONFLICT (id) DO NOTHING;

  -- 3. Job. job_number is auto-generated by set_job_number ->
  -- next_job_number, which uses AAA's per-org counter from build47.
  -- Status 'in_progress' + damage 'water' so the capture FAB has a
  -- realistic target.
  INSERT INTO public.jobs (
    id, organization_id, contact_id,
    status, urgency, damage_type, damage_source,
    property_address, property_type
