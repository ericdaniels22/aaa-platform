DO $$
DECLARE
  aaa_org_id      uuid := 'a0000000-0000-4000-8000-000000000001';
  test_user_id    uuid := 'bc93b88d-a74a-4d92-b36d-9f74bf58925f';
  test_contact_id uuid := 'c0000000-0000-4000-8000-000000000001';
  test_job_id     uuid := 'b0000000-0000-4000-8000-000000000001';
BEGIN
  -- 1. Membership: link the dashboard-created test user to AAA.
  INSERT INTO public.user_organizations (user_id, organization_id, role, is_active)
